// Vowel or Consonant using switch case 

#include<stdio.h>
void main()

{
    char abcd;
    printf("Enter The Character - ");
    scanf("%c",&abcd);

    switch (abcd)
    {
    case 'A':

    case 'a':

    case 'E':

    case 'e':

    case 'I':

    case 'i':

    case 'O':

    case 'o':

    case 'U':

    case 'u':
            printf("\n This Character is Vowels");
        break;
    
    default:
            printf("\n This Character is Consonents");
        break;
    }
}